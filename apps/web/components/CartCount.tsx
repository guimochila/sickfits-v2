import styled from '@emotion/styled'
import { TransitionGroup, CSSTransition } from 'react-transition-group'
import Dot from './styled/Dot'

const CartCountAnimation = styled.span`
  position: relative;
  .count {
    display: block;
    position: relative;
    transition: transform 0.4s;
    backface-visibility: hidden;
  }
  .count-enter {
    transform: scale(4) rotateX(0.5turn);
  }
  .count-enter-active {
    transform: rotateX(0);
  }
  .count-exit {
    top: 0;
    position: absolute;
    transform: rotateX(0);
  }
  .count-exit-active {
    transform: scale(4) rotateX(0.5turn);
  }
`

interface ICartCountProps {
  count: number
}

function CartCount({ count }: ICartCountProps) {
  return (
    <CartCountAnimation>
      <TransitionGroup>
        <CSSTransition
          unmountOnExit
          className="count"
          classNames="count"
          key={count}
          timeout={{ enter: 400, exit: 400 }}
        >
          <Dot>{count}</Dot>
        </CSSTransition>
      </TransitionGroup>
    </CartCountAnimation>
  )
}

export default CartCount
